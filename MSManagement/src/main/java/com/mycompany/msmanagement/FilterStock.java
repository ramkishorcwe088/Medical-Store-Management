    public void getStock()
    {
        Connection conn=new GetConnection().getConnection();
        //String text=jTextField1.getText();
        PreparedStatement ps;
        ResultSet rs=null;
        try {
            ps=conn.prepareStatement("select name, available from medicine where name=?");
            ps.setString(1, jTextField1.getText());
            rs=ps.executeQuery(); 
            rs.next();
            medicineNameTextField.setText(rs.getString("name"));
            availableTextField.setText(rs.getInt("available")+"");
            
        } catch (SQLException ex) {
            Logger.getLogger(FirstCharacter.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
 
    
    
